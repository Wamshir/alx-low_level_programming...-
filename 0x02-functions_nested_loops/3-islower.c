/**                                       
* _islower - checks the lower characters
 * @c : the lower character             
 * Return: 1 if letter is lower case; 0 if not
 */
void _islower(int c)
{                                                if (c >= 'a' && c <= 'z')
                return (1);
        else                                             return (0);
}
